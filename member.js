function skillsMember() {
  var member = document.getElementById("member");
  var skills = document.getElementById("skills");
  var memberText = document.getElementById("memberText");
  var skillsText = document.getElementById("skillsText");
  var memberBorder = document.getElementById("memberBorder");
  var skillsBorder = document.getElementById("skillsBorder");
  member.style.color = "#000000";
  skills.style.color = "#ffffff";
  memberText.style.display = "block";
  skillsText.style.display = "none";
  memberBorder.style.display = "block";
  skillsBorder.style.display = "none";
}